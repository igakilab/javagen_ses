class ShopItemB8 {
    private String itemName;
    private int price;
    private int stock;

    public ShopItemB8(String itemName, int price, int stock) {
        this.itemName = itemName;
        this.price = price;
        this.stock = stock;
    }

    public String getItemName() {
        return itemName;
    }

    public int getPrice() {
        return price;
    }

    public int getStock() {
        return stock;
    }
}